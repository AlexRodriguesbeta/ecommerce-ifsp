package model;

public class FinishingProcess{
    private int idFinishing;
    private String finishingProcess; 

    public int getIdFinishing() {
        return this.idFinishing;
    }

    public void setIdFinishing(int idFinishing) {
        this.idFinishing = idFinishing;
    }

    public String getFinishingProcess() {
        return this.finishingProcess;
    }

    public void setFinishingProcess(String finishingProcess) {
        this.finishingProcess = finishingProcess;
    }
} 
